    private void updateTotal() {
        double total = 0.0;
        DefaultTableModel model = (DefaultTableModel) tbProduk.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            total += (double) model.getValueAt(i, 3); // kolom subtotal
        }
        jLabel9.setText(String.format("%.2f", total));
    }
    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String nama = tfNama.getText();
        String hargaStr = tfHargaBeli.getText();
        String jumlahStr = tfJumlah.getText();

        if (nama.isEmpty() || hargaStr.isEmpty() || jumlahStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field produk wajib diisi.");
            return;
        }

        try {
            double harga = Double.parseDouble(hargaStr);
            int jumlah = Integer.parseInt(jumlahStr);
            double subtotal = harga * jumlah;

            DefaultTableModel model = (DefaultTableModel) tbProduk.getModel();
            model.addRow(new Object[]{nama, harga, jumlah, subtotal});

            // Update total
            updateTotal();

            // Kosongkan field input
            tfNama.setText("");
            tfHargaBeli.setText("");
            tfJumlah.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga dan jumlah harus berupa angka.");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnCetakOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakOrderActionPerformed
        StringBuilder sb = new StringBuilder();
        sb.append("===== DETAIL ORDER =====\n");
        sb.append("ID Order : ").append(tfIdOrder.getText()).append("\n");
        sb.append("Supplier : ").append(cbMitra.getSelectedItem()).append("\n");
        sb.append("\nDaftar Produk:\n");

        DefaultTableModel model = (DefaultTableModel) tbProduk.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            sb.append("- ").append(model.getValueAt(i, 0)).append(", ");
            sb.append("Harga: ").append(model.getValueAt(i, 1)).append(", ");
            sb.append("Jumlah: ").append(model.getValueAt(i, 2)).append(", ");
            sb.append("Subtotal: ").append(model.getValueAt(i, 3)).append("\n");
        }

        sb.append("\nTotal: Rp. ").append(jLabel9.getText());

        taDetailOrder.setText(sb.toString());
    }//GEN-LAST:event_btnCetakOrderActionPerformed

