    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tbProduk.getSelectedRow();

        if (selectedRow >= 0) {
            try {
                String nama = tfNama.getText();
                double harga = Double.parseDouble(tfHargaBeli.getText());
                int jumlah = Integer.parseInt(tfJumlah.getText());
                double subtotal = harga * jumlah;

                DefaultTableModel model = (DefaultTableModel) tbProduk.getModel();
                model.setValueAt(nama, selectedRow, 0);
                model.setValueAt(harga, selectedRow, 1);
                model.setValueAt(jumlah, selectedRow, 2);
                model.setValueAt(subtotal, selectedRow, 3); 

                // Kosongkan field input
                tfNama.setText("");
                tfHargaBeli.setText("");
                tfJumlah.setText("");

                // Update total keseluruhan
                updateTotal();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Harga dan jumlah harus berupa angka.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diupdate terlebih dahulu.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tbProduk.getSelectedRow();

        if (selectedRow >= 0) {
            DefaultTableModel model = (DefaultTableModel) tbProduk.getModel();
            model.removeRow(selectedRow);
            
            updateTotal();
            
            tfNama.setText("");
            tfHargaBeli.setText("");
            tfJumlah.setText("");
            taDetailOrder.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus terlebih dahulu.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

